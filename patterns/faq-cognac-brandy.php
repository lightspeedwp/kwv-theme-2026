<p class="has-200-font-size" style="line-height:var(--wp--custom--line-height--body)">Cognac is a type of brandy made in a particular way, governed and guided by the Bureau National Interprofessionnel du Cognac.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph {"fontSize":"200","style":{"typography":{"lineHeight":"var:custom|line-height|body"}}} -->
<p class="has-200-font-size" style="line-height:var(--wp--custom--line-height--body)">Cognac (Brandy, particularly South African Brandy) is similar in that it also adheres to strict methods in production, as determined by the Liquor Products Act. The other difference is the region. Just like Champagne can only be called as such if made in the region and according to specific set standards, production of Cognac must be in the Cognac region of France. Production of Brandy can, however, be in any other area or place. One similarity they both have is that both Cognac and South African Brandy must be made from grapes.</p>
